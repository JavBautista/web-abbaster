<h3>1. UTILIZAR UNA WI- FI O CONEXIÓN A INTERNET SEGURA</h3>
<p>Aunque sea cómodo y fácil navegar y comprar conectado a redes de bares, centros comerciales o tiendas, lo cierto es que estas redes suelen ser poco seguras. Los paquetes de información transmitidos a través de las conexiones públicas pueden ser capturados fácilmente por hackers o ciberdelincuentes.  Por este motivo, para realizar compras u operaciones en los que vayas a transferir información personal importante, te recomendamos que lo hagas a través de una red segura.</p>

<h3>2. MANTÉN TU EQUIPO ACTUALIZADO Y PROTEGIDO</h3>
<p>Realizar compras online es uno de los procesos en los que más información personal y peligrosa transferimos. Por ese motivo, antes de empezar a comprar asegúrate de tener tu equipo seguro, actualizado y de tener instalado un buen producto antivirus.</p>

<h3>3. COMPRA EN SITIOS WEB CONOCIDOS Y CON BUENA REPUTACIÓN</h3>
<p>Buscando en la red la mejor oferta podemos llegar a sitios webs desconocidos y poco fiables. Aléjate de ellos.  Comprueba que la url del sitio coincide con la web donde crees está y que su dirección empieza por https. Comprando en web fiables y de reconocida reputación online evitarás ser víctima del robo de datos o identidad.</p>

<h3>4. BUSCA Y REVISA LA POLÍTICA DE PRIVACIDAD Y DE DEVOLUCIONES</h3>
<p>Antes de comprar en una tienda online revisa que tienen su política de privacidad en un lugar visible y está actualizada. ¿Sabes qué tienes que hacer si tu regalo no es el adecuado? ¿Cómo puedes devolverlo? Estos también son aspectos a tener en cuenta a la hora de elegir dónde comprar.</p>

<h3>5. DA SOLO LOS DATOS PERSONALES NECESARIOS Y HAZLO DE FORMA SEGURA</h3>
<p>Si te extraña que te pidan algún dato personal (móvil, nombre de familiares, lugar de naciemiento…) para efectuar la compra, no lo hagas y revisa que estás en el sitio correcto. Además, herramientas como el teclado virtual de Panda Security puede evitarte el robo de datos si tu ordenador está infectado por algún virus espía.</p>

<h3>6. EVITA LOS ENLACES DE CORREOS Y ANUNCIOS </h3>
<p>La oferta que acabas de recibir al correo ¿es demasiado buena para ser verdad? Entonces, posiblemente no lo sea. En estas fechas, las campañas de anuncios y mails con ofertas y grandes descuentos son una herramienta muy utilizada por los ciberdelincuentes. Evita el phishing.</p>

<h3>7. REVISA PERIÓDICAMENTE LOS MOVIMIENTOS DE TU TARJETA DE CRÉDITO</h3>
<p>Después de hacer varias compras online, revisa que todos los cargos de tu cuenta son conocidos y los tienes controlados. Si alguno te resulta sospechoso, ponte en contacto con tu banco, quizás hayas sido víctima de un robo de identidad.</p>
